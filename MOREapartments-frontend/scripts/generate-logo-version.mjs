import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.join(__dirname, "../public");
const outputFile = path.join(__dirname, "../src/generated/logo.ts");
const headerIconFile = path.join(publicDir, "logo-header-icon.png");
const headerSourceFile = path.join(publicDir, "logo-header.png");

function logoVersion(fileName) {
  const filePath = path.join(publicDir, fileName);
  return fs.existsSync(filePath)
    ? Math.floor(fs.statSync(filePath).mtimeMs)
    : Date.now();
}

async function generateHeaderIcon() {
  if (!fs.existsSync(headerSourceFile)) {
    return;
  }

  const metadata = await sharp(headerSourceFile).metadata();
  const width = metadata.width ?? 0;
  const height = metadata.height ?? 0;

  if (!width || !height) {
    return;
  }

  const cropSize = Math.min(width, height);
  const left = Math.floor((width - cropSize) / 2);
  const top = Math.floor((height - cropSize) / 2);

  await sharp(headerSourceFile)
    .extract({ left, top, width: cropSize, height: cropSize })
    .resize(512, 512, { fit: "cover" })
    .png({ compressionLevel: 9 })
    .toFile(headerIconFile);
}

function writeLogoModule() {
  const logoVersionValue = logoVersion("logo.png");
  const headerIconVersionValue = logoVersion("logo-header-icon.png");

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(
    outputFile,
    `/* Auto-generated by scripts/generate-logo-version.mjs — do not edit manually */
export const LOGO_SRC = "/logo.png?v=${logoVersionValue}";
export const LOGO_HEADER_SRC = "/logo-header-icon.png?v=${headerIconVersionValue}";
`,
    "utf8",
  );
}

async function generate() {
  await generateHeaderIcon();
  writeLogoModule();
  console.log(`Generated logo cache keys -> ${outputFile}`);
}

generate().catch((error) => {
  console.error(error);
  process.exit(1);
});

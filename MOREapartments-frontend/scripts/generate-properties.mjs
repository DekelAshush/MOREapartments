import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.join(__dirname, "../public");
const outputFile = path.join(__dirname, "../src/data/properties.ts");

const IMAGE_EXT = /\.(jpe?g|png|webp|gif)$/i;

function slugify(address) {
  return address
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}

function parseApartmentFolder(name) {
  const match = name.match(/^(\d+)\s*(.*)$/i);
  if (!match) {
    return { number: name, label: "" };
  }

  const label = match[2].trim();
  return {
    number: Number(match[1]),
    label: label || "",
  };
}

function pickCoverImage(files) {
  const priority = ["סלון", "מטבח", "חדר שינה", "גינה", "מרפסת"];
  for (const keyword of priority) {
    const match = files.find((file) => file.includes(keyword));
    if (match) return match;
  }
  return files[0];
}

function encodePublicPath(...segments) {
  return `/${segments.map((segment) => encodeURIComponent(segment)).join("/")}`;
}

function scanProperties() {
  const addresses = fs
    .readdirSync(publicDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b, "he"));

  return addresses.map((address) => {
    const addressDir = path.join(publicDir, address);
    const apartmentDirs = fs
      .readdirSync(addressDir, { withFileTypes: true })
      .filter((entry) => entry.isDirectory());

    const apartments = apartmentDirs
      .map((entry) => {
        const { number, label } = parseApartmentFolder(entry.name);
        const apartmentDir = path.join(addressDir, entry.name);
        const images = fs
          .readdirSync(apartmentDir)
          .filter((file) => IMAGE_EXT.test(file))
          .sort((a, b) => a.localeCompare(b, "he"));

        const coverImage = pickCoverImage(images);

        return {
          id: `${slugify(address)}-${number}`,
          folderName: entry.name,
          number,
          label,
          labelKey: label ? label.toLowerCase().replace(/\s+/g, "-") : "",
          imageCount: images.length,
          coverImage: encodePublicPath(address, entry.name, coverImage),
          images: images.map((image) =>
            encodePublicPath(address, entry.name, image),
          ),
        };
      })
      .sort((a, b) => Number(a.number) - Number(b.number));

    return {
      id: slugify(address),
      address,
      apartmentCount: apartments.length,
      coverImage: apartments[0]?.coverImage ?? "",
      apartments,
    };
  });
}

function generate() {
  const properties = scanProperties();
  const contents = `/* Auto-generated by scripts/generate-properties.mjs — do not edit manually */
export type Apartment = {
  id: string;
  folderName: string;
  number: number;
  label: string;
  labelKey: string;
  imageCount: number;
  coverImage: string;
  images: string[];
};

export type Property = {
  id: string;
  address: string;
  apartmentCount: number;
  coverImage: string;
  apartments: Apartment[];
};

export const properties: Property[] = ${JSON.stringify(properties, null, 2)};

export const propertyCountBySize = properties.reduce<Record<number, number>>(
  (counts, property) => {
    counts[property.apartmentCount] =
      (counts[property.apartmentCount] ?? 0) + 1;
    return counts;
  },
  {},
);
`;

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, contents, "utf8");
  console.log(`Generated ${properties.length} properties -> ${outputFile}`);
}

generate();
